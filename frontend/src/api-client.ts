export const fetchCurrentUser = async (): Promise<UserType> => {
  const response = await fetch(`${API_BASE_URL}/api/users/me`, {
    credentials: "include",
  });
  if (!response.ok) {
    throw new Error("Error fetching user");
  }
  return response.json();
};

// *********FETCH REQUEST REGISTER********
// Sends a POST request to register a new user to the server 
// the server will send back a response 
// and the response gets stored in the response variable
export const register = async (formData: RegisterFormData) => {
  const response = await fetch(`${API_BASE_URL}/api/users/register`, {
    method: "POST", // because we're creating a user
    credentials: "include",
    headers: {
      "Content-Type": "application/json",//telling the server to expect a json format
    },
    body: JSON.stringify(formData),
  });

  // getting the body of the response because we send back depending on the type of error that we get from the backend
  const responseBody = await response.json();

  if (!response.ok) {
    throw new Error(responseBody.message);
  }
};

